function agregar() {
    const conteiner = document.querySelector('#conteiner');
    const form = document.querySelector('#form');
    const icon = document.querySelector('#icon');
    const btnCircle = document.querySelector('#btnCircle');
    const btnCircle2 = document.querySelector('#btnCircle2');
    const button = document.querySelector('#button');
    const text = document.querySelector('#text');

    conteiner.setAttribute('class', 'conteiner');
    form.setAttribute('class', 'form');
    icon.setAttribute('class', 'icon');
    btnCircle.setAttribute('class', 'btnCircle');
    btnCircle2.setAttribute('class', 'btnCircle');
    button.setAttribute('class', 'button');
    text.setAttribute('class', 'text');
}